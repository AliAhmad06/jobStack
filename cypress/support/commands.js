Cypress.Commands.add('login', (Email_Address,Password) => {
    cy.visit('https://career.orcaloholding.co.uk/login')
    cy.get('[type = "email"]').type(Email_Address)
    cy.get('[id = "password"]').type(Password)
    cy.contains('[type="submit"]', 'Login').click()
    
})
